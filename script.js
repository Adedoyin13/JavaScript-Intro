//Operators

let a = 10;
let d = 5;

// Addition

let sum = a + d ;

console.log(sum);


// Subtraction

let subtraction = a - d ;

console.log(subtraction);


// Multiplication

let multiply = a * d ;

console.log(typeof multiply);


// Division

let divide = a / d ;

console.log(divide);


// Modulus (reminder)

let remainder = a % d ;

console.log(remainder);


// Exponentiation (reminder)

let power = a ** 2 ;

console.log(power);

